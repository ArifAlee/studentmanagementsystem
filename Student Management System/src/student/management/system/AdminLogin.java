    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:

        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to update?","Update Information",JOptionPane.YES_NO_OPTION);
        if(p==0){

            try{
                int row =student_table.getSelectedRow();
                String value =(student_table.getModel().getValueAt(row, 0).toString());
                String sql= "UPDATE Students set firstname=?,lastname=?,email=?,"
                        + "mobile=?,course=?"
                        + "WHERE userid="+value;
                pst=conn.prepareStatement(sql);
                pst.setString(1,firstnametxt.getText());
                pst.setString(2,lastnametxt.getText());
                pst.setString(3,emailtxt.getText());
                pst.setString(4,mobiletxt.getText());
                pst.setString(5,coursenametxt.getText());
                pst=conn.prepareStatement(sql);
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Student Updated");

            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }

            Update_table();
        }
    }//GEN-LAST:event_btn_updateActionPerformed

