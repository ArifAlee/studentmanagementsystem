    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete record?","Delete",JOptionPane.YES_NO_OPTION);
        if(p==0){
            // if user chooses to delete data this code will run
        int row =student_table.getSelectedRow();
        String value =student_table.getModel().getValueAt(row, 0).toString();
        student_table.setModel(DbUtils.resultSetToTableModel(rs));
        
        String sql ="DELETE from Students where userid='"+value+"' ";
            try{
                pst=conn.prepareStatement(sql);
                pst.executeUpdate();

             
                JOptionPane.showMessageDialog(null,"Record Deleted");
                DefaultTableModel model = (DefaultTableModel)student_table.getModel();
                model.setRowCount(0);
            }catch(Exception e){

                JOptionPane.showMessageDialog(null, e);
            }finally {

                try{
                    rs.close();
                    pst.close();

                }
                catch(Exception e){

                }
            }
            Update_table();

        }
    }//GEN-LAST:event_btn_deleteActionPerformed

